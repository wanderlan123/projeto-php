<h2 class="subtitle">O que é uma TO-DO list?</h2>
<p class="text">Uma to do list é uma lista de tarefas, de coisas que precisam ser feitas. É uma forma de organizar as atividades que não podem ser esquecidas, e que não estão inseridas na nossa rotina.</p>

<p class="text">É diferente de uma agenda, onde as atividades tem dia e hora para acontecer. Ela trata das atividades, simples e importantes, que precisamos resolver ao longo do nosso dia.</p>
