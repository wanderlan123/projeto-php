<h2 class="subtitle">Projeto feito em PHP para a diciplina de<br><i>Desenvolvimento Web</i></h2>
<p class="text">O tema escolhido foi TO-DO list, utilizando apenas o HTML, CSS, PHP e o MySQL para o desenvolvimento</p>

<p class="text">O projeto trabalha com um sistema de autenticação simples para o acesso da lista, e do painel de administrativo, onde apenas usuários com permissão poderam entrar e gerenciar os usuários e as mensagens enviadas por eles a partir do "Fale Conosco".
</p>

